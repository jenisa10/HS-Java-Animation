public class varExercise
{
    public static void main(String[] args) {
        int beds = 12;
        System.out.println(beds + " beds");
        int dogs = 6;
        System.out.println(dogs + " dogs");
        int sum = beds + dogs;
        System.out.println(sum + " is the sum of beds and dogs");
        int difference = beds - dogs;
        System.out.println(difference + " is the difference of beds and dogs");
        int quotient = beds / dogs;
        System.out.println(quotient + " is the quotient of beds and dogs");
        int product = beds * dogs;
        System.out.println(product + " is the product of beds and dogs");
    }
}